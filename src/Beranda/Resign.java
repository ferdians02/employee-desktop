    private void loadData() {
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("NIK");
    model.addColumn("Nama Karyawan");
    model.addColumn("Tanggal");
    model.addColumn("Keterangan");
    model.addColumn("Status");
    model.addColumn("Keterangan SPV");

    try {
        String sql = """
            SELECT
                r.id_resign,
                k.nik,
                k.nama_karyawan,
                r.tanggal,
                r.ket_resign,
                r.status_resign,
                r.approval_spv_desc1
            FROM tb_resign r
            JOIN tb_karyawan k ON r.id_karyawan = k.id_karyawan
            WHERE r.id_karyawan = ?
            ORDER BY r.id_resign DESC
        """;

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, loginn.idKaryawanLogin); // ðŸ’¡ ambil ID user yang login
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_resign"),
                rs.getString("nik"),
                rs.getString("nama_karyawan"),
                rs.getDate("tanggal"),
                rs.getString("ket_resign"),
                rs.getString("status_resign"),
                rs.getString("approval_spv_desc1") != null ? rs.getString("approval_spv_desc1") : "-"
            });
        }

        tbl.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load data resign: " + e.getMessage());
    }
}







