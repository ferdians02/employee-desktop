    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        String nikInput = nik.getText();
        java.util.Date tglAwal = awal.getDate();
        java.util.Date tglAkhir = akhir.getDate();
        String alasanCuti = alasan.getText();

        // Validasi input kosong
        if (nikInput.isEmpty() || tglAwal == null || tglAkhir == null || alasanCuti.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap lengkapi semua isian!");
            return;
        }

        // Validasi tanggal
        long beda = tglAkhir.getTime() - tglAwal.getTime();
        long jumlahHari = (beda / (1000 * 60 * 60 * 24)) + 1;
        if (jumlahHari <= 0) {
            JOptionPane.showMessageDialog(this, "Tanggal akhir harus setelah tanggal awal.");
            return;
        }

        // Validasi maksimal cuti 12 hari
        if (jumlahHari > 12) {
            JOptionPane.showMessageDialog(this, "Durasi cuti maksimal 12 hari.");
            return;
        }

        try {
            // Ambil ID dan jabatan dari NIK
            String sqlKaryawan = """
                SELECT k.id_karyawan, j.nama_jabatan
                FROM tb_karyawan k
                JOIN tb_jabatan j ON k.id_jabatan = j.id_jabatan
                WHERE k.nik = ?
            """;
            PreparedStatement ps1 = conn.prepareStatement(sqlKaryawan);
            ps1.setString(1, nikInput);
            ResultSet rs1 = ps1.executeQuery();

            if (!rs1.next()) {
                JOptionPane.showMessageDialog(this, "Data karyawan tidak ditemukan.");
                return;
            }

            int idKaryawan = rs1.getInt("id_karyawan");
            String namaJabatan = rs1.getString("nama_jabatan");

            // Validasi cuti overlap
            String sqlCheckOverlap = """
                SELECT COUNT(*) FROM tb_cuti
                WHERE id_karyawan = ?
                  AND record_flag = 'N'
                  AND (
                      (? BETWEEN tgl_awal AND tgl_akhir) OR
                      (? BETWEEN tgl_awal AND tgl_akhir) OR
                      (tgl_awal BETWEEN ? AND ?) OR
                      (tgl_akhir BETWEEN ? AND ?)
                  )
            """;
            PreparedStatement psCheck = conn.prepareStatement(sqlCheckOverlap);
            psCheck.setInt(1, idKaryawan);
            psCheck.setDate(2, new java.sql.Date(tglAwal.getTime()));
            psCheck.setDate(3, new java.sql.Date(tglAkhir.getTime()));
            psCheck.setDate(4, new java.sql.Date(tglAwal.getTime()));
            psCheck.setDate(5, new java.sql.Date(tglAkhir.getTime()));
            psCheck.setDate(6, new java.sql.Date(tglAwal.getTime()));
            psCheck.setDate(7, new java.sql.Date(tglAkhir.getTime()));
            ResultSet rsCheck = psCheck.executeQuery();

            if (rsCheck.next() && rsCheck.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Tanggal cuti bentrok dengan pengajuan sebelumnya.");
                return;
            }

            // Simpan data ke tb_cuti
            String sqlInsert = """
                INSERT INTO tb_cuti 
                (id_karyawan, tgl_awal, tgl_akhir, alasan, lama_cuti, create_by, create_at, record_flag, approval_spv_by1, status_cuti)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """;
            PreparedStatement ps2 = conn.prepareStatement(sqlInsert);
            ps2.setInt(1, idKaryawan);
            ps2.setDate(2, new java.sql.Date(tglAwal.getTime()));
            ps2.setDate(3, new java.sql.Date(tglAkhir.getTime()));
            ps2.setString(4, alasanCuti);
            ps2.setInt(5, (int) jumlahHari);
            ps2.setString(6, namaJabatan); // create_by
            ps2.setDate(7, new java.sql.Date(System.currentTimeMillis())); // create_at
            ps2.setString(8, "N"); // record_flag
            ps2.setString(9, null); // approval_spv_by1 (belum diset)
            ps2.setString(10, "Waiting for approve"); // status_cuti âœ… WAJIB

            ps2.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pengajuan cuti berhasil disimpan.");
            kosong();
            tampilData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data cuti: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_simpanActionPerformed

