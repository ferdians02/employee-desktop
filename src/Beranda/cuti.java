            private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Nama Karyawan");
    model.addColumn("Tgl Awal");
    model.addColumn("Tgl Akhir");
    model.addColumn("Alasan");
    model.addColumn("Lama");
    model.addColumn("Status");
    model.addColumn("Persetujuan"); // <--- Tambahan

    try {
        String sql = """
            SELECT 
                c.id_cuti,
                k.nama_karyawan,
                c.tgl_awal,
                c.tgl_akhir,
                c.alasan,
                c.lama_cuti,
                c.status_cuti,
                c.approval_spv_desc1
            FROM tb_cuti c
            JOIN tb_karyawan k ON c.id_karyawan = k.id_karyawan
            WHERE c.record_flag = 'N' AND c.id_karyawan = ?
        """;

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, loginn.idKaryawanLogin); // ambil ID karyawan yang login
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_cuti"),
                rs.getString("nama_karyawan"),
                rs.getDate("tgl_awal"),
                rs.getDate("tgl_akhir"),
                rs.getString("alasan"),
                rs.getInt("lama_cuti"),
                rs.getString("status_cuti"),
                rs.getString("approval_spv_desc1") != null ? rs.getString("approval_spv_desc1") : "-" // jika NULL, tampil "-"
            });
        }

        tbl.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
    }
}




